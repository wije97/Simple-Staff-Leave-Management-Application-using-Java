    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int row = tblSalary.getSelectedRow();

        if(row == -1)
        {
            JOptionPane.showMessageDialog(null, "Please select a Member");
        }
        else{
                String id = (tblSalary.getModel().getValueAt(row, 0).toString());
                String qry = "DELETE FROM salary_tbl WHERE id = '" + id + "' ";
                try
                {
                    pst=con.prepareStatement(qry);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Deleted Success");

                    clear();
                    fetchDataFromDB();
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null,e);
                }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblSalaryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSalaryMouseClicked

        int row = tblSalary.getSelectedRow();
        String id = (tblSalary.getModel().getValueAt(row, 0).toString());
        
        try
        {
            String qry2 = "SELECT * FROM salary_tbl WHERE id = '" + id + "'";

            pst = con.prepareStatement(qry2);
            rs = pst.executeQuery();
            if(rs.next())
            {
                jt1.setText(rs.getString("St_Id"));
                jt2.setText(rs.getString("Name"));
                jt3.setText(rs.getString("Leave_count"));
                jt4.setText(rs.getString("Per_day_charge"));
                jt5.setText(rs.getString("Basic_salary"));
                jtxtcal1.setText(rs.getString("leave_charge"));
                jtxtcal2.setText(rs.getString("Total_salary"));
                
                btnInsert.setEnabled(false);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tblSalaryMouseClicked

