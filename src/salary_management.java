    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

         if(!(jt1.getText().equals("")) && !(jt2.getText().equals("")) && !(jt3.getText().equals("")) && !(jt4.getText().equals("")) && !(jt5.getText().equals("")) && !(jtxtcal1.getText().equals("")) && !(jtxtcal2.getText().equals("")))
        {
            int row = tblSalary.getSelectedRow();
            String id = (tblSalary.getModel().getValueAt(row,0).toString());

            try
            {
                DateFormat df = new SimpleDateFormat();
                java.util.Date today = new java.util.Date();
                String stoday = df.format(today);
               
                String qry=" UPDATE salary_tbl SET St_Id = ?, Name = ?, Leave_count = ?, Per_day_charge = ?, Basic_salary = ?, leave_charge = ?, Total_salary = ?, Date = ? WHERE id = '" + id + "'";
                pst = con.prepareStatement(qry);
                pst.setString(1, jt1.getText());
                pst.setString(2, jt2.getText());
                pst.setString(3, jt3.getText());
                pst.setString(4, jt4.getText());
                pst.setString(5, jt5.getText());
                pst.setString(6, jtxtcal1.getText());
                pst.setString(7, jtxtcal2.getText());
                pst.setString(8, stoday);
                pst.execute();
                
                JOptionPane.showMessageDialog(null, "Data Updated... ");
                clear();
                fetchDataFromDB();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

