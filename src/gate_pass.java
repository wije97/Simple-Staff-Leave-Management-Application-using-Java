    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int row = tblGatePass.getSelectedRow();

        if(row == -1)
        {
            JOptionPane.showMessageDialog(null, "Please select a Gate Pass");
        }
        else
        {

            
            String id = (tblGatePass.getModel().getValueAt(row, 0).toString());
            String qry = "DELETE FROM gate_pass WHERE id = '" + id + "' ";
            try
            {
                pst=con.prepareStatement(qry);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Deleted Success");

                clear();
                fetchDataFromDB();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
            
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        
        if(!(txtDName.getText().equals("")) && !(txtVNO.getText().equals("")) && !(txtPlace.getText().equals("")) && !(txtFrom.getText().equals("")) && !(txtTo.getText().equals("")) && !(txtPurpose.getText().equals("")))
        {   
            String qry;
            try
            {
                Format formatter = new SimpleDateFormat("yyyy-MM-dd");
                String date = formatter.format(dcDate.getDate());
                
                qry="INSERT INTO gate_pass (St_Id, DriverName, VehicleNo, Place, Date, t_From, t_To, Purpose ) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
                pst = con.prepareStatement(qry);
                pst.setString(1, staffIDH.getText());
                pst.setString(2, txtDName.getText());
                pst.setString(3, txtVNO.getText());
                pst.setString(4, txtPlace.getText());
                pst.setString(5, date);
                pst.setString(6, txtFrom.getText());
                pst.setString(7, txtTo.getText());
                pst.setString(8, txtPurpose.getText());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Stored... ");
                fetchDataFromDB();
                clear();
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please fill all fields.");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

