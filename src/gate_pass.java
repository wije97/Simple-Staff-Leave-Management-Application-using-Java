    private void tblGatePassMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGatePassMouseClicked

        int row = tblGatePass.getSelectedRow();
        String id = (tblGatePass.getModel().getValueAt(row, 0).toString());

        try
        {
            String qry2 = "SELECT * FROM gate_pass WHERE id = '" + id + "'";

            pst = con.prepareStatement(qry2);
            rs = pst.executeQuery();
            if(rs.next())
            {
                txtDName.setText(rs.getString("DriverName"));
                txtVNO.setText(rs.getString("VehicleNo"));
                txtPlace.setText(rs.getString("Place"));
                dcDate.setDate(rs.getDate("Date"));
                txtFrom.setText(rs.getString("t_From"));
                txtTo.setText(rs.getString("t_To"));
                txtPurpose.setText(rs.getString("Purpose"));
                btnSubmit.setEnabled(false);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tblGatePassMouseClicked

