    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        
        String qry = "";
        
        if(!(txtReason.getText().equals("")))
        {
            try
            {
                
                qry = " INSERT INTO leave_apply (stid,ltype,sdate,edate,reason,status) VALUES (?, ?, ?, ?, ?, ?)";
                pst = con.prepareStatement(qry);
                pst.setString(1,staffIDH.getText());
                pst.setString(2,cmbLeaveType.getSelectedItem().toString());
                pst.setDate(3,dateconvert(dcStartDate.getDate()));
                pst.setDate(4,dateconvert(dcEndDate.getDate()));
                pst.setString(5,txtReason.getText());
                pst.setString(6,"Pending");
                pst.execute();
                JOptionPane.showMessageDialog(null, "Request Send...");
                clear();
                fetchDataFromDB();
                
               
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please Enter Reason");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        if(!(txtReason.getText().equals("")))
        {  int row = tblLeaveResult.getSelectedRow();
            String id = (tblLeaveResult.getModel().getValueAt(row,0).toString());
            try
            {
                String qry=" UPDATE leave_apply SET ltype = ?, sdate = ?, edate = ?, reason = ?, status = ? WHERE id = '" + id + "'";
                pst=con.prepareStatement(qry);

                pst.setString(1, cmbLeaveType.getSelectedItem().toString());
                pst.setDate(2,dateconvert(dcStartDate.getDate()));
                pst.setDate(3,dateconvert(dcEndDate.getDate()));
                pst.setString(4,txtReason.getText());
                pst.setString(5,"Pending");
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Updated... ");
                clear();
                fetchDataFromDB();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        int row = tblLeaveResult.getSelectedRow();
        
        if(row == -1)
        {
            JOptionPane.showMessageDialog(null, "Please select a Leave");
        }
        else{
            
            String status = (tblLeaveResult.getModel().getValueAt(row, 7).toString());
            
            if(status.equals("Accepted")){
                JOptionPane.showMessageDialog(null, "This Leave was Accepted. You can not Delete...!");
            }
            else{
                String id = (tblLeaveResult.getModel().getValueAt(row, 0).toString());
                String qry = "DELETE FROM leave_apply WHERE id = '" + id + "' ";
                try
                {
                    pst=con.prepareStatement(qry);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Deleted Success");

                    clear();
                    fetchDataFromDB();
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null,e);
                }
            }
        }
        
       
    }//GEN-LAST:event_btnDeleteActionPerformed
