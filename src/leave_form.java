    private void tblLeaveResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLeaveResultMouseClicked
        
        int row = tblLeaveResult.getSelectedRow();
        String id = (tblLeaveResult.getModel().getValueAt(row, 0).toString());
        
        try
        {
            String status = (tblLeaveResult.getModel().getValueAt(row, 7).toString());

            if(status.equals("Accepted")){
                JOptionPane.showMessageDialog(null, "This Leave was Accepted. You can not change...!");
            }
            else{
                
                String qry2 = "SELECT id, ltype, sdate, edate, reason FROM leave_apply WHERE id = '" + id + "'";

                pst = con.prepareStatement(qry2);
                rs = pst.executeQuery();
                if(rs.next())
                {
                    cmbLeaveType.setSelectedItem(rs.getString("ltype"));
                    dcStartDate.setDate(rs.getDate("sdate"));
                    dcEndDate.setDate(rs.getDate("edate"));
                    txtReason.setText(rs.getString("reason"));
                    btnSubmit.setEnabled(false);
                }
            }
             
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tblLeaveResultMouseClicked
 
