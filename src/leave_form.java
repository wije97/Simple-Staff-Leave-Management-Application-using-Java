    public void fetchDataFromDB(){
        
        String sql = "SELECT leave_apply.id, staff_info.Staff_Id AS `Stafff ID`, staff_info.Name , leave_apply.ltype, leave_apply.sdate, leave_apply.edate, leave_apply.Reason \n " +
                " , leave_apply.status FROM leave_apply INNER JOIN staff_info ON leave_apply.stid = staff_info.Staff_Id WHERE stid = '" + staffIDH.getText() + "'";
        try
        {
          pst = con.prepareStatement(sql); 
          rs = pst.executeQuery(sql);
          tblLeaveResult.setModel(DbUtils.resultSetToTableModel(rs));
          
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public static java.sql.Date dateconvert(java.util.Date date)
    {
        if(date != null)
        {
            java.sql.Date dd=new java.sql.Date(date.getTime());
            return dd;
        }
        return null;
    }
    
