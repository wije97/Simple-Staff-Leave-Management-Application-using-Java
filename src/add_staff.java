    private void tblStaffTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStaffTableMouseClicked
        int row = tblStaffTable.getSelectedRow();
        String id = (tblStaffTable.getModel().getValueAt(row, 0).toString());
        try
        {
            String qry = "SELECT id, Staff_Id AS `Stafff ID`, Name , Age, Gender, Department, DOB, Address, Mobile, Password FROM staff_info WHERE id = '" + id + "'";

            pst = con.prepareStatement(qry);
            rs = pst.executeQuery();
            if(rs.next())
            {
                txtName.setText(rs.getString("Name"));
                txtAge.setText(rs.getString("Age"));
                String gender=rs.getString("Gender");
                
                if(gender.equals("Male"))
                {
                    rbtMale.setSelected(true);
                }
                else
                {
                    rbtFemale.setSelected(true);
                }
                dcDoB.setDate(rs.getDate("DOB"));
                cmbDepartment.setSelectedItem(rs.getString("Department"));
                txtAddress.setText(rs.getString("Address"));
                txtMobile.setText(rs.getString("Mobile"));
                txtPassword.setText(rs.getString("Password"));
                btnInsert.setEnabled(false);
                txtSID.setEnabled(false);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tblStaffTableMouseClicked

