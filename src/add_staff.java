    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
       
        if(!(txtName.getText().equals("")) && !(txtSID.getText().equals("")) && !(txtAge.getText().equals("")) && !(txtMobile.getText().equals("")) && !(txtAddress.getText().equals("")) && !(txtPassword.getText().equals("")))
        {
            try
            {
                String qry;
                
                String gender = "";
                if(rbtMale.isSelected())
                {
                    gender = "Male";
                }
                else if(rbtFemale.isSelected())
                {
                    gender = "Female";
                }
                
                qry="INSERT INTO staff_info (Staff_Id, Name, Age, Gender, Department, DOB, Address, Mobile, Password ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
                pst = con.prepareStatement(qry);
                pst.setString(1, txtSID.getText());
                pst.setString(2, txtName.getText());
                pst.setString(3, txtAge.getText());
                pst.setString(4, gender);
                pst.setString(5, cmbDepartment.getSelectedItem().toString());
                Date d=dateconvert(dcDoB.getDate());
                pst.setDate(6, d);
                pst.setString(7, txtAddress.getText());
                pst.setString(8, txtMobile.getText());
                pst.setString(9, txtPassword.getText());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Stored... ");
                clearStaff();
                loadStafftData();
                
                
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Pls Fill All the Fields");
        }
        
    }//GEN-LAST:event_btnInsertActionPerformed
    public static java.sql.Date dateconvert(java.util.Date date)
    {
        if(date != null)
        {
            java.sql.Date dd = new java.sql.Date(date.getTime());
            return dd;
        }
        return null;
    }
    
